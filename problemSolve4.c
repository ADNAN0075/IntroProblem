#include<stdio.h>
int main()
{
    int num1=5;
    float num2=3.141593;
    char ch= 'a';

    printf("The integer value :%d\n",num1);
    printf("The floating point value :%f\n",num2);
    printf("The character value :%c\n",ch);

    int num3 = 100;
    float num4=1.618000;
    char ch1= 'z';

    printf("The integer value :%d\n",num3);
    printf("The floating point value :%f\n",num4);
    printf("The character value :%c\n",ch1);


    return 0;
}
