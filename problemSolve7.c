#include<stdio.h>
int main()
{
    int a;
    printf("Enter your integer number :");
    scanf("%d",&a);
    float b;
    printf("Enter your floating number :");
    scanf("%f",&b);
    char ch;
    printf("Enter your character :");
    scanf(" %c",&ch);
    printf("The integer value :%d\n",a);
    printf("The floating value :%f\n",b);
    printf("The character value :%c\n",ch);
    return 0;
}


